# String Slicing
names = "Sujal,Karan,Pradeep,Kartik"
print(names[0:5])
print(names[6:11])
print(names[12:19])
print(names[20:26])
print(len(names))

fruit = "Mango"
len1 = len(fruit)
print(fruit)
print(len1)
print("Mango is a ", len1 ,"letter word")

pie = "Applepiee"
print(pie[:5])
print(pie[6])
print(pie[0:len(pie)-3])
print(pie[:len(pie)-3])
print(pie[-1:len(pie)-3])
print(pie[0:-3])
print(pie[-3:-1])

nm = "Sujal"
print(nm[-4:-2])