# for loops
name = "Abhishek"
for i in name:
    print(i,end=",")

colors = ["red","orange","pink","green","yellow","blue"]
for color in colors:
    print(color)
    for i in color:
        print(i)
        
for k in range(5):
    print(k)
for i in range(1,12):
    print(i)
for r in range(1,2000):
    print(r+1)
for j in range(1,12, 2):
    print(j)

